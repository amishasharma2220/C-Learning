#include<stdio.h> //This is header file which contains the declaration of printf function .This let us use the input and output functions.
// This is the simplest C program that prints "Hello World" to the console.
int main(){ 
    
    // main function is the entry point of the program. The execution of the program starts from here.
    printf("Hello World");
    // printf function is used to print the output to the console. It takes a string as an argument and prints it to the console.
    printf("Have a nice day!"); 
    // In C we can print multiple statements using printf function. Here we are printing "Have a nice day!" after "Hello World".
 // Every statement in C should end with a semicolon (;). It is used to separate the statements in the program.
    return 0; // return statement is used to return a value from the main function. Here we are returning 0 which indicates that the program has executed successfully.
} 